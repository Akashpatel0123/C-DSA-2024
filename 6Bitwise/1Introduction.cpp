//Bitwise Operator
#include<iostream>
using namespace std;
int main()
{
   cout<<"Bitwise AND : "<<(3 & 5)<<endl;
   cout<<"Bitwise OR : "<<(3 | 5)<<endl;
   cout<<"Bitwise XOR : "<<(3 ^ 5)<<endl;
   cout<<"Binary NOT : "<<(~6)<<endl;
   cout<<"Left Shift :"<<(7<<2)<<endl;

   return 0;
}